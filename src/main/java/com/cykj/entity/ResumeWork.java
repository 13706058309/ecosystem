package com.cykj.entity;

/**
 * 工作 ----简历
 */
public class ResumeWork {

  private long rwId;
  private long weId;
  private long resumeId;


  public ResumeWork() {
  }

  public long getRwId() {
    return rwId;
  }

  public void setRwId(long rwId) {
    this.rwId = rwId;
  }


  public long getWeId() {
    return weId;
  }

  public void setWeId(long weId) {
    this.weId = weId;
  }


  public long getResumeId() {
    return resumeId;
  }

  public void setResumeId(long resumeId) {
    this.resumeId = resumeId;
  }

}
